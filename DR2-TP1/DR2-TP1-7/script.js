let n1 = Number(prompt("Informe o primeiro numero inteiro:"));
let n2 = Number(prompt("Informe o segundo numero inteiro:"));

let soma = n1 + n2;
let sub = n1 - n2;
let mult = n1 * n2;
let div = n1 / n2;

alert(
  "Resultado dos calculos" +
    "\n\nSoma: " +
    soma +
    "\nSubtracao: " +
    sub +
    "\nMultiplicacao: " +
    mult +
    "\nDivisao: " +
    div
);

